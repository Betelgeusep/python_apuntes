num_uno=10
num_dos=20
""" > Mayor que: Verdadero si el operando izquierdo es mayor que el derecho [x > y]
    < Menor que: Verdadero si el operando izquierdo es menor que el derecho [x < y]
    >= Mayor o igual que True si el operando de la izquierda es mayor o igual que el de la derecha[x >= y]
    <= Menor o igual que True si el operando de la izquierda es menor o igual que el de la derecha [x <=y]
    == Igual a: Verdadero si ambos operandos son iguales [x == y]
    != No es igual a: verdadero si los operandos no son iguales [x != y]
"""
resulto=num_uno<num_dos
print(resulto)

# = es un operador de asignación y == operador de comparación

#----------------------ejemplos----------------------
a = 13
b = 33
 
# a > b es False
print(a > b)
 
# a < b es True
print(a < b)
 
# a == b es False
print(a == b)
 
# a != b is True
print(a != b)
 
# a >= b es False
print(a >= b)
 
# a <= b es True
print(a <= b)